package com.example.entity;

public class MineDynamic {
    String name;
    int number;
    public MineDynamic(String name, int number)
    {
        this.name = name;
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNumber() {
        return number;
    }

}
